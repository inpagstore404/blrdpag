#include <iostream>

using namespace std;

int main(){


	//for loop increment
	cout << "Loop 1\n";
	for (int i = 1; i <= 10; i++){
			cout << i << endl;
		}	

	cout << "\n\n";

	//for loop decrement
	cout << "Loop 2\n";
	for (int d = 1; d >= -10; d--){
			cout << d << endl;
		}	
}